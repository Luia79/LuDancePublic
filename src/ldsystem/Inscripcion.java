    private void btnInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscribirActionPerformed
        String nombreal, apellidoal, direccional;
         

        nombreal = txtNombre.getText();
        apellidoal = txtApellido.getText();
        direccional = txtDireccion.getText();
        Integer telefonoal = Integer.parseInt(this.txtTelefono.getText());

        if (nombreal.equals(" ") || apellidoal.equals(" ") || direccional.equals(" ") || telefonoal.equals(" ")){

            JOptionPane.showMessageDialog(null, "Campos vacío ");

        }else {

            JOptionPane.showMessageDialog(null, "Agregado ");

        }
    }//GEN-LAST:event_btnInscribirActionPerformed

    private void txtApellidoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtApellidoKeyTyped
        char validar = evt.getKeyChar();

        if (!Character.isLetter(validar) && !Character.isWhitespace(validar)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null, "Ingrese solo letras y espacios");
            txtNombre.setText(" ");
        }
    }//GEN-LAST:event_txtApellidoKeyTyped

